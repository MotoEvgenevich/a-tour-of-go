package main

import "fmt"

func main() {
	fmt.Println("1 + 2 =", 1+2)   // Addition - Сложение
	fmt.Println("5 * 2 =", 5*2)   // Multiplication - Умножение
	fmt.Println("10 - 2 =", 10-2) // Subtraction - Вычетание
	fmt.Println("10 / 2 =", 10/2) // Division - Деление
	fmt.Println("10 % 6 =", 10%6) // Remainder - Остаток
}
